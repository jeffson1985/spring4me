package org.osforce.spring4me.demo.flow;

public class FlowBean {

	private String name;
	
	private String extras;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getExtras() {
		return extras;
	}

	public void setExtras(String extras) {
		this.extras = extras;
	}
	
}
